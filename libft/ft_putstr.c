#include "libft.h"

void	ft_putstr(const char *str)
{
	int	i;

	i = 0;
	if (!str)
	{
		ft_putstr("(null)");
		return ;
	}
	while (str[i] != '\0')
		ft_putchar(str[i++]);
}
